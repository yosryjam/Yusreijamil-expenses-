export function categorize(merchant, customRules = []) {
  const m = (merchant || "").toLowerCase();
  for (const [pat, cat] of [...customRules, ...DEFAULT_RULES]) {
    if (m.includes(pat.toLowerCase())) return cat;
  }
  return "שונות";
}

